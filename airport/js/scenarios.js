// יוצר מופע סיטואציה לנוסע מסוים (פותר מחרוזות דינמיות).
function instantiate(s, p) {
  return {
    id: s.id, label: s.label, sev: s.sev,
    alert: typeof s.alert === 'function' ? s.alert(p) : s.alert,
    prompt: typeof s.prompt === 'function' ? s.prompt(p) : s.prompt,
    options: s.options.map((o) => ({ ...o })),
    resolved: false, resolvedOk: false,
  };
}

// מגריל 0-2 סיטואציות מתאימות לנוסע.
export function rollScenarios(p) {
  const pool = SCENARIOS.filter((s) => !s.applies || s.applies(p));
  const roll = Math.random();
  const n = roll < 0.22 ? 0 : roll < 0.82 ? 1 : 2;
  const copy = [...pool]; const chosen = [];
  for (let i = 0; i < n && copy.length; i++) {
    const idx = Math.floor(Math.random() * copy.length);
    chosen.push(instantiate(copy.splice(idx, 1)[0], p));
  }
  return chosen;
}
